package HW_2_test_video_exercise;

public class Arithmetics {

    public int add(int a, int b) {
        return a + b;
    }

    public int deduct(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b) {
        return a / b;
    }

}
